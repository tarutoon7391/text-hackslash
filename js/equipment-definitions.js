/* ==============================================================
   蒼銀の剣 — 段階式MP回復量計算
   ============================================================== */

/**
 * 蒼銀の剣の MP 回復量をプレイヤーレベルに応じて段階的に計算する
 * - Lv1〜99  : Lv × 0.5
 * - Lv100〜199: 50 + (Lv - 100) × 0.2
 * - Lv200〜499: 70 + (Lv - 200) × 0.1
 * - Lv500    : 上限 100 固定
 * 端数切り捨て・最低 1 を適用した整数値を返す
 * @param {number} level - プレイヤーレベル
 * @returns {number} 計算後のMP回復量（整数・最低1）
 */
function calcAoginMpRegen(level) {
  let raw;
  if (level >= 500) {
    raw = 100;
  } else if (level >= 200) {
    raw = 70 + (level - 200) * 0.1;
  } else if (level >= 100) {
    raw = 50 + (level - 100) * 0.2;
  } else {
    raw = level * 0.5;
  }
  return Math.max(1, Math.floor(raw));
}

/* ==============================================================
   成長型武器のステータス計算
   ============================================================== */

/**
 * 成長型装備のレベル別ステータスを計算して返す
 * 計算式: 各ステータス = lv × growthCoeff（レベル連動型）
 * 蒼銀の剣の例: ATK = lv×2.0 / DEF = lv×0.8 / HP = lv×1.5 / MP = lv×2.0
 * Lv50時点:  ATK100/DEF40/HP75/MP100
 * Lv99時点:  ATK198/DEF79/HP148/MP198（D12エンド武器ATK195より強くレジェンドには届かない）
 * Lv200時点: ATK400/DEF160/HP300/MP400（D24エンド武器より低く、D24レジェンドには届かない）
 * 強化ボーナスは呼び出し側で factor = 1 + 0.1 × enhLv として乗算する
 * @param {object} eq    - 装備定義（isGrowth: true のもの）
 * @param {number} level - プレイヤーレベル
 * @returns {{attack:number, defense:number, maxHp:number, maxMp:number}}
 */
function computeGrowthStats(eq, level) {
  if (!eq.isGrowth) return eq.stats;
  const lv = Math.max(1, Math.min(500, level || 1));
  const coeff = eq.growthCoeff || { attack: 0, defense: 0, maxHp: 0, maxMp: 0 };
  return {
    attack:  Math.floor(lv * coeff.attack),
    defense: Math.floor(lv * coeff.defense),
    maxHp:   Math.floor(lv * coeff.maxHp),
    maxMp:   Math.floor(lv * coeff.maxMp),
  };
}
