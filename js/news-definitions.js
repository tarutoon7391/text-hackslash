'use strict';

/* ==============================================================
   ニュースデータ定義
   - 最大10件まで保持。10件を超えた場合は古いものから自動削除。
   - id は一意の文字列、date は 'YYYY-MM-DD' 形式。
   ============================================================== */

const NEWS_DEFINITIONS = (function () {
  const items = [
    {
      id: 'news_001',
      date: '2026-03-30',
      title: 'ニュース機能を追加しました！',
      content: 'アップデート情報をお知らせする機能を追加しました。',
    },
  ];

  // 日付の降順でソートしたうえで最大10件に絞る
  return items
    .slice()
    .sort((a, b) => (a.date < b.date ? 1 : a.date > b.date ? -1 : 0))
    .slice(0, 10);
})();
